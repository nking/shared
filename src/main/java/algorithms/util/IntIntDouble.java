package algorithms.util;

/**
 *
 * @author nichole
 */
public class IntIntDouble {
    private final int a;
    private final int b;
    private final double c;

    /**
     *
     @param a
     @param b
     @param c
     */
    public IntIntDouble(int a, int b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    /**
     *
     @return
     */
    public int getA() {
        return a;
    }

    /**
     *
     @return
     */
    public int getB() {
        return b;
    }

    /**
     *
     @return
     */
    public double getC() {
        return c;
    }
}
